    public static List<String> getNearestAppointments() {
        List<String> announcements = new java.util.ArrayList<>();

        String sql = "SELECT department, office, reason, date, time " +
                     "FROM APPOINTMENT " +
                     "ORDER BY ABS(DATEDIFF(date, CURDATE())) ASC " +
                     "LIMIT 4";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            try (java.sql.Connection con = java.sql.DriverManager.getConnection(URL, USER, PASS);
                 java.sql.Statement stmt = con.createStatement();
                 java.sql.ResultSet rs = stmt.executeQuery(sql)) {

                while (rs.next()) {
                    announcements.add(
                        rs.getString("department") + " | " +
                        rs.getString("office") + "\n" +
                        rs.getString("reason") + " | " +
                        rs.getString("date") + " | " +
                        rs.getString("time")
                    );
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        return announcements;
    }
