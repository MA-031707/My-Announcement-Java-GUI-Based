    private void searchboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchboxActionPerformed
        // TODO add your handling code here:
         String keyword = searchbox.getText().trim();

    if (keyword.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter something to search.");
        return;
    }

    String url = "jdbc:mysql://localhost:3306/Login_db";
    String user = "root";
    String dbPass = "root";

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        java.sql.Connection conn =
            java.sql.DriverManager.getConnection(url, user, dbPass);

        String sql =
            "SELECT * FROM appointment " +
            "WHERE LOWER(department) LIKE LOWER(?) " +
            "OR LOWER(professor) LIKE LOWER(?) " +
            "OR CAST(time AS CHAR) LIKE ?";

        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, "%" + keyword + "%");
        pst.setString(2, "%" + keyword + "%");
        pst.setString(3, "%" + keyword + "%"); // ‚Üê THIS WAS MISSING

        java.sql.ResultSet rs = pst.executeQuery();

        StringBuilder results = new StringBuilder();
        while (rs.next()) {
            results.append("Dept: ").append(rs.getString("department"))
                   .append(", Prof: ").append(rs.getString("professor"))
                   .append(", Date: ").append(rs.getString("date"))
                   .append(", Time: ").append(rs.getString("time"))
                   .append("\n");
        }

        if (results.length() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "No appointments found.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, results.toString());
        }

        conn.close();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_searchboxActionPerformed

